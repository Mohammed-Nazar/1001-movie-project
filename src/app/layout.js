import { Inter } from "next/font/google";
import "./globals.css";
import Sidebar from "@/components/Sidebar";
import SearchSide from "@/components/SearchSide/SearchSide";
import { popularMovies, topRatedMovies } from "@/API/mainApi";
import Skeleton from "react-loading-skeleton";
import "react-loading-skeleton/dist/skeleton.css";
import { Suspense } from "react";

const inter = Inter({ subsets: ["latin"] });

export const metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({ children }) {
  const popularMoviesData = await popularMovies(1);
  const topRatedMoviesData = await topRatedMovies(1);
  return (
    <html lang="en">
      <body className={inter.className}>
        <Suspense fallback={<Skeleton count={50} highlightColor="#898996"/>}>
          <Sidebar />
          {children}
          <SearchSide
            popularMoviesData={popularMoviesData}
            topRatedMoviesData={topRatedMoviesData}
          />
        </Suspense>
      </body>
    </html>
  );
}
